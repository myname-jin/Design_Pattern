    // [기능 3] 외부(Controller)에서 선택된 값들을 가져오기 위한 Getter 메서드들
    // 1. 선택된 날짜 문자열로 합쳐서 반환 (예: "2025-11-23")
    public String getSelectedDate() {
        String y = (String) Year.getSelectedItem();
        String m = (String) Month.getSelectedItem();
        String d = (String) Day.getSelectedItem();

        // 월, 일이 한 자리 수일 경우 0 붙이기 (선택 사항)
        if (m.length() == 1) {
            m = "0" + m;
        }
        if (d.length() == 1) {
            d = "0" + d;
        }

        return y + "-" + m + "-" + d;
    }

