    // [기능 2] 룸 타입에 따라 룸 번호 콤보박스 내용을 바꾸는 메서드
    private void updateRoomNumbers() {
        String selectedType = (String) RoomType.getSelectedItem();

        // 기존 항목 싹 지우기 (초기화)
        RoomNum.removeAllItems();

        if ("강의실".equals(selectedType)) {
            // 강의실일 경우의 목록
            String[] classrooms = {"911", "912", "913"};
            for (String room : classrooms) {
                RoomNum.addItem(room);
            }
        } else if ("실습실".equals(selectedType)) {
            // 실습실일 경우의 목록
            String[] labs = {"914", "915", "916"};
            for (String room : labs) {
                RoomNum.addItem(room);
            }
        } else {
            // "전체" 또는 선택 안됨일 경우 모든 방 표시
            String[] allRooms = {"911", "912", "913", "914", "915", "916", "918"};
            for (String room : allRooms) {
                RoomNum.addItem(room);
            }
        }

        selectedRoom.setText((String) RoomNum.getSelectedItem());

    }

